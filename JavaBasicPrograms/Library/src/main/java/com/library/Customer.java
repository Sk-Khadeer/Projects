package com.library;

public class Customer {
private int id,total_no_books;
private String customer_name,city,date_of_join,email;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getTotal_no_books() {
	return total_no_books;
}
public void setTotal_no_books(int total_no_books) {
	this.total_no_books = total_no_books;
}
public String getCustomer_name() {
	return customer_name;
}
public void setCustomer_name(String customer_name) {
	this.customer_name = customer_name;
}
public String getCity() {
	return city;
}
public void setCity(String city) {
	this.city = city;
}
public String getDate_of_join() {
	return date_of_join;
}
public void setDate_of_join(String date_of_join) {
	this.date_of_join = date_of_join;
}
public String getEmail() {
	return email;
}
public void setEmail(String email) {
	this.email = email;
} 
}
